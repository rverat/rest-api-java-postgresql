    private void listCategory() {

        List<CategoryRs> lista = new ArrayList<>();

        try {
            lista = doInBackground();
        } catch (Exception ex) {
            Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
        }

        String cabecera[] = {"ID", "NOMBRE"};
        DefaultTableModel table = new DefaultTableModel(null, cabecera);
        String[] registros = new String[2];
        for (CategoryRs response : lista) {
            registros[0] = String.valueOf(response.getCategoryid());
            registros[1] = response.getName();

            table.addRow(registros);
        }
        tbCategory.setModel(table);
    }

    protected List<CategoryRs> doInBackground() throws Exception {
        CategoryService categoryService = RetrofitClient.getClient("http://localhost:9080").create(CategoryService.class);
        Call<List<CategoryRs>> call = categoryService.getAllCategories();
        Response<List<CategoryRs>> response = call.execute();

        return response.body();
    }

    protected void save(CategoryRs categoryRs) throws Exception {
        CategoryService categoryService = RetrofitClient.getClient("http://localhost:9080").create(CategoryService.class);
        Call<HttpStatus> call = categoryService.save(categoryRs);
        call.execute();

    }

    protected void delete(int id) throws Exception {
        CategoryService categoryService = RetrofitClient.getClient("http://localhost:9080").create(CategoryService.class);
        Call<HttpStatus> call = categoryService.delete(id);
        call.execute();

    }

