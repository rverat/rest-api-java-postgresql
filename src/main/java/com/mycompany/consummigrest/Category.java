    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (lblId.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccionar Categoria\na Eliminar", "Atentamente Sistema", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int id = Integer.parseInt(lblId.getText().trim());

        try {
            delete(id);
            JOptionPane.showMessageDialog(null, "Eliminado");
            listCategory();
        } catch (Exception ex) {
            Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "ERROR EN REGISTRAR");
        }


    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyActionPerformed

        CategoryRs category = new CategoryRs();

        category.setCategoryid(Integer.parseInt(lblId.getText()));
        category.setName(txtNombre.getText());
        try {

            save(category);
            txtNombre.setText("");
            lblId.setText("");
            listCategory();

        } catch (Exception e) {
            Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(null, "ERROR EN REGISTRAR");
        }
    }//GEN-LAST:event_btnModifyActionPerformed

